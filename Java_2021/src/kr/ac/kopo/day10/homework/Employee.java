package kr.ac.kopo.day10.homework;

public class Employee {
	int no;
	String name;
	int salary;
	String grade;
	
	public Employee() {
		super();
		
	}
	public Employee(int no, String name, int salary, String grade) {
		super();
		this.no = no;
		this.name = name;
		this.salary = salary;
		this.grade = grade;
	}
	
}
