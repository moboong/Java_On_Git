package kr.ac.kopo.day12.inter01;

public class LGTV {

	public LGTV() {
		System.out.println("LGTV를 구매하셨습니다.");
	}

	public void turnOn() {
		System.out.println("TV전원 ON");
	}

	public void turnOff() {
		System.out.println("TV전원 OFF");
	}

	public void channelUp() {
		System.out.println("채널을 올립니다.");
	}

	public void channelDown() {
		System.out.println("채널을 내립니다.");
	}

	public void soundUp() {
		System.out.println("사운드를 올립니다.");
	}

	public void soundDown() {
		System.out.println("사운드를 내립니다.");
	}
}
